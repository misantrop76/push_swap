#include "libft.h"

int	ft_atoi(const char *str)
{
	int			i;
	int			signe;
	long long	nb;

	i = 0;
	while (str[i] == ' ')
		i++;
	signe = 1;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			signe *= -1;
		i++;
	}
	nb = 0;
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + (str[i] - 48);
		i++;
	}
	return (nb * signe);
}
