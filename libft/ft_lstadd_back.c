#include "libft.h"

void	ft_lstadd_back(t_list **alst, t_list *new)
{
	t_list	*last;

	last = ft_lstlast(*alst);
	if (!(last == NULL))
	{
		last->next = new;
		new->next = NULL;
	}
	else
		ft_lstadd_front(alst, new);
}
